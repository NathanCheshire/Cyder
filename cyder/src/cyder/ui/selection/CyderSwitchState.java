package cyder.ui.selection;

/**
 * The possible states for a {@link CyderSwitch}.
 */
public enum CyderSwitchState {
    /**
     * On the right of the bounding box.
     */
    ON,

    /**
     * On the left of the bounding box.
     */
    OFF,

    /**
     * In the middle of the bounding box.
     */
    INDETERMINATE
}
